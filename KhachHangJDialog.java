    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        KhachHang kh = new KhachHang();
        String HoTen = txtHoTen.getText().trim();
        String SDT = txtSDT.getText().trim();
        String DiaChi = txtDiaChi.getText().trim();
        Date NgayDK = jdcNgayDK.getDate();

        List<String> errors = new ArrayList<>();

        if (HoTen.isEmpty()) {
            errors.add("Vui lòng nhập họ tên khách hàng");
        } else if (!HoTen.matches("^[a-zA-ZÀ-ỹ ]+$")) {
            errors.add("Họ tên không được chứa số và ký tự đặc biệt");
        } else {
            kh.setHoTen(HoTen);
        }

        if (SDT.isEmpty()) {
            errors.add("Vui lòng nhập số điện thoại khách hàng");
        } else if (!SDT.matches("\\d+")) {
            errors.add("Số điện thoại chỉ được chứa chữ số");
        } else if (SDT.length() > 10) {
            errors.add("Số điện thoại không được dài quá 10 số");
        } else {
            kh.setSDT(SDT);
        }

        if (DiaChi.isEmpty()) {
            errors.add("Vui lòng nhập địa chỉ khách hàng");
        } else {
            kh.setDiaChi(DiaChi);
        }

        if (NgayDK == null) { // Changed isEmpty() to null check for Date
            errors.add("Vui lòng chọn ngày đăng ký");
        } else {
            kh.setNgayDK(NgayDK);
        }

        if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            errors.add("Vui lòng chọn giới tính!");
        } else {
            kh.setGioiTinh(rdoNam.isSelected());
        }

        // Hiển thị lỗi nếu có
        if (!errors.isEmpty()) {
            StringBuilder errorMessage = new StringBuilder("Lỗi nhập liệu:\n");
            for (String error : errors) {
                errorMessage.append("- ").append(error).append("\n");
            }
            JOptionPane.showMessageDialog(this, errorMessage.toString(), "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);
            return; // Dừng nếu có lỗi
        }

        try {
            dao.insert(kh);
            fillTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!"); // Changed "nhân viên" to "khách hàng"
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thêm thất bại! Lỗi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed

        int selectedRow = tblKhachHang.getSelectedRow();
        if (selectedRow >= 0) {
            KhachHang kh = dao.selectAll().get(selectedRow);
            kh.setHoTen(txtHoTen.getText());
            kh.setSDT(txtSDT.getText());
            kh.setDiaChi(txtDiaChi.getText());
            kh.setGioiTinh(rdoNam.isSelected());
            dao.update(kh);
            fillTable();
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần cập nhật!");
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

