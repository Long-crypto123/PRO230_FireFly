    void fillTable() {
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0); //Xóa hết dòng
        List<KhachHang> ds = dao.selectAll();
        for (KhachHang kh : ds) {
            Object[] row = {
                kh.getMaKH(),
                kh.getHoTen(),
                kh.getSDT(),
                kh.getDiaChi(),
                kh.getGioiTinh() ? "Nữ" : "Nam",
                XDate.toString(kh.getNgayDK(), "yyyy-MM-dd"),};
            model.insertRow(0, row);
        }
    }

